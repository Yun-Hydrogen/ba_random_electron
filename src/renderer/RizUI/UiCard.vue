<!--
================================================================================
  组件：UiCard.vue
  所属：UI 组件库（src/renderer/RizUI）
  类型：布局容器组件

================================================================================
  一、功能
================================================================================
  统一的配置面板卡片容器。每个 Tab 页面由多张 UiCard 组成。
  提供标题 + 描述文字 + 内容插槽的标准布局。

================================================================================
  二、Props
================================================================================
  title  — 卡片标题（必填）
  desc   — 描述文字，传入空字符串则不显示描述区

================================================================================
  三、Slot
================================================================================
  default — 卡片内容区，放置 cfg-row 等控件

================================================================================
  四、使用示例
================================================================================
  <UiCard title="浮窗外观" desc="调整抽取结果浮窗的视觉样式">
    <div class="cfg-row">...</div>
  </UiCard>

  <UiCard title="联动" desc="">
    <div class="cfg-row">...</div>
  </UiCard>

================================================================================
  五、维护注意事项
================================================================================
  - 本组件不包含业务逻辑，仅提供布局容器
  - 样式使用 scoped，不会污染调用方
  - 修改卡片外观（圆角、背景、间距）只需改这一个文件

  最后更新：2026-07-21
================================================================================
-->
<template>
  <div class="ui-card">
    <div class="ui-card-title">{{ title }}</div>
    <div v-if="desc" class="ui-card-desc">{{ desc }}</div>
    <slot />
  </div>
</template>

<script setup>
defineProps({
  title: { type: String, required: true },
  desc:  { type: String, default: '' }
})
</script>

