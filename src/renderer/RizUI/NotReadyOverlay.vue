<!--
================================================================================
  组件：NotReadyOverlay.vue
  所属：UI 组件库（src/renderer/RizUI）
  类型：通用遮罩组件

================================================================================
  一、功能
================================================================================
  在父容器上覆盖一个半透明毛玻璃遮罩，居中显示提示文字。
  用于标记"功能等待实现"或"暂不可用"的 UI 区域。

================================================================================
  二、Props
================================================================================
  text    — 遮罩上显示的文字，默认 "🚧 等待实现"

================================================================================
  三、使用方法
================================================================================
  1. 父容器必须设置 position: relative（本组件不强制，由调用方控制）
  2. 将 <NotReadyOverlay> 放在需要遮罩的容器内部任意位置

  示例：
    <div style="position: relative">
      ...正常内容...
      <NotReadyOverlay text="功能开发中" />
    </div>

================================================================================
  四、CSS 原理（教学用）
================================================================================
  - position: absolute + inset: 0 → 铺满最近的 relative 父容器
  - background: rgba(255,255,255,0.55) → 半透明白色雾化（不影响子元素透明度）
  - backdrop-filter: blur(1.5px) → 对遮罩后内容做毛玻璃模糊
  - z-index: 1 → 压在内容上方，但低于 dialog（9999）

  最后更新：2026-07-21
================================================================================
-->
<template>
  <div class="nro-overlay">
    <span class="nro-badge">{{ text }}</span>
  </div>
</template>

<script setup>
defineProps({
  text: { type: String, default: '🚧 等待实现' }
})
</script>

<style scoped>
.nro-overlay {
  position: absolute;
  inset: 0;
  z-index: 1;
  display: flex;
  align-items: center;
  justify-content: center;
  background: rgba(255, 255, 255, 0.55);
  backdrop-filter: blur(1.5px);
  /* 防止遮罩本身的模糊边缘溢出父容器（父容器需设 overflow: hidden） */
}

.nro-badge {
  padding: 6px 16px;
  border-radius: 999px;
  background: rgba(0, 0, 0, 0.55);
  color: #fff;
  font-size: 12px;
  font-weight: 600;
  letter-spacing: 0.5px;
  /* 徽章不受毛玻璃影响（它是遮罩的子元素，在遮罩"上面"） */
}
</style>
